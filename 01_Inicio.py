import streamlit as st

st.title("Encuesta Permanente de Hogares")

st.write("La Encuesta Permanente de Hogares (EPH) recopila información socioeconómica de la población argentina, con especial foco en el mercado laboral. A través de entrevistas realizadas en hogares seleccionados, la EPH registra datos sobre empleo, desempleo, subocupación, condiciones laborales, cobertura social y otros aspectos relacionados con el trabajo. Además, incluye información detallada sobre las características del hogar y la vivienda, lo que permite analizar las condiciones habitacionales de la población. Esta información se actualiza de forma continua a lo largo del año y es fundamental para elaborar indicadores que reflejan la realidad social, laboral y habitacional del país.")


